package Bit_Manipulation;

public class que1 {
    public static void main(String[] args) {
        System.out.println(5 & 6); // 1 & 1 is 1 rest all are 0 (Bitwise AND)
        System.out.println(5 | 6); // 0 | 0 is 0 rest all are 1 (Bitwise OR)
        System.out.println(5 ^ 6); // 0 ^ 0 and 1 ^ 1 is 0 rest all are 1 (Bitwise XOR)
        System.out.println(~5); // NOT Operator (Bitwise One's complement)
    }
}
